    private void btnRunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRunActionPerformed
        // TODO add your handling code here:
        if (comboboxWhatToDo.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "You need to choose an action!",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String doing = comboboxWhatToDo.getSelectedItem().toString();

        if (doing.equals("Add")) {
            if (txtEnrollmentID.getText().length() == 0 || txtStaffID.getText().length() == 0
                    || txtRoom.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "No field can be empty!",
                        "Warning!", JOptionPane.WARNING_MESSAGE);
            } else {
                try {
                    if (Checking_Input_Data.Check("enrollment_ID", txtEnrollmentID.getText(),
                            "Class_allocation", false)
                            && !Checking_Input_Data.Check("enrollment_ID", txtEnrollmentID.getText(),
                            "Check_student_enrollment",
                            "Student must register before can be being allocated!", true)){
                        Connection con = ConnectionClass.connection();
                        String findStudentID =
                                "SELECT * FROM Check_student_enrollment WHERE enrollment_ID = "
                                        + txtEnrollmentID.getText();
                        Statement statement = con.createStatement();
                        ResultSet resultSet = statement.executeQuery(findStudentID);
                        resultSet.next();
                        String tempStudentID = resultSet.getString("student_ID");
                        int temp2StudentID = resultSet.getInt("student_ID");

                        String findClassID =
                                "SELECT * FROM Student WHERE student_ID = " + temp2StudentID;
                        Statement statement1 = con.createStatement();
                        ResultSet resultSet1 = statement1.executeQuery(findClassID);
                        resultSet1.next();
                        String tempClassID = resultSet1.getString("class_ID");

                        String findSubjectID =
                                "SELECT * FROM Check_student_enrollment WHERE enrollment_ID = '"
                                        + txtEnrollmentID.getText()
                                        +"'";
                        Statement statement2 = con.createStatement();
                        ResultSet resultSet2 = statement2.executeQuery(findSubjectID);
                        resultSet2.next();
                        String tempSubjectID = resultSet2.getString("subject_ID");
                        con.close();
                        if (Checking_Input_Data.Check("staff_ID", txtStaffID.getText(), "Staff", true)){
                            JOptionPane.showMessageDialog(null,
                                    "This Staff is not available, \ncheck who's available at Staff button in menu.",
                                    "Warning!", JOptionPane.WARNING_MESSAGE);
                            txtStaffID.selectAll();
                            txtStaffID.replaceSelection("");
                            return;
                        }
                        Connection connection = ConnectionClass.connection();
                        String insertQuery = "INSERT INTO Class_allocation VALUES (? ,? ,? ,? ,? ,? )";
                        PreparedStatement preparedStatement = connection.prepareStatement(insertQuery);
                        preparedStatement.setString(1, txtEnrollmentID.getText());
                        preparedStatement.setString(2, tempSubjectID);
                        preparedStatement.setString(3, txtStaffID.getText());
                        preparedStatement.setString(4, tempStudentID);
                        preparedStatement.setString(5, tempClassID);
                        preparedStatement.setString(6, txtRoom.getText());

                        preparedStatement.executeUpdate();
                        if (showTime) {
                            show_allocation();
                            showTime = false;
                        } else {
                            model.setRowCount(0);
                            show_allocation();
                        }
                        JOptionPane.showMessageDialog(null, "Insert completed!");
                        connection.close();
                    } else {
                        txtEnrollmentID.selectAll();
                        txtEnrollmentID.replaceSelection("");
                    }
                } catch (SQLException e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        } else if (doing.equals("Update")) {
            if (txtEnrollmentID.getText().length() == 0 || txtStaffID.getText().length() == 0
                    || txtRoom.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "No field can be empty!",
                        "Warning!", JOptionPane.WARNING_MESSAGE);
            } else {
                try {
                    if (!Checking_Input_Data.Check("enrollment_ID", txtEnrollmentID.getText(),
                            "Class_allocation", true)){
                        if (Checking_Input_Data.Check("staff_ID", txtStaffID.getText(), "Staff", true)){
                            JOptionPane.showMessageDialog(null,
                                    "This Staff is not available, \ncheck who's available at Staff button in menu.",
                                    "Warning!", JOptionPane.WARNING_MESSAGE);
                            txtStaffID.selectAll();
                            txtStaffID.replaceSelection("");
                            return;
                        }

                        Connection connection = ConnectionClass.connection();
                        String insertQuery = "UPDATE Class_allocation SET staff_ID = ?, room = ? WHERE enrollment_ID = ?";
                        PreparedStatement preparedStatement = connection.prepareStatement(insertQuery);
                        preparedStatement.setString(3, txtEnrollmentID.getText());
                        preparedStatement.setString(1, txtStaffID.getText());
                        preparedStatement.setString(2, txtRoom.getText());

                        preparedStatement.executeUpdate();
                        if (showTime) {
                            show_allocation();
                            showTime = false;
                        } else {
                            model.setRowCount(0);
                            show_allocation();
                        }
                        JOptionPane.showMessageDialog(null,
                                "Update completed! \nTips: Only Staff ID and Room can be updated!");
                        connection.close();
                    } else {
                        txtEnrollmentID.selectAll();
                        txtEnrollmentID.replaceSelection("");
                    }
                } catch (SQLException e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        } else if (doing.equals("Remove")) {
            if (txtEnrollmentID.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Enrollment ID cannot be empty!",
                        "Warning!", JOptionPane.WARNING_MESSAGE);
            } else {
                try {
                    if (!Checking_Input_Data.Check("enrollment_ID", txtEnrollmentID.getText(),
                            "Class_allocation", true)){
                        Connection connection = ConnectionClass.connection();
                        String insertQuery = "DELETE FROM Class_allocation WHERE enrollment_ID = ?";
                        PreparedStatement preparedStatement = connection.prepareStatement(insertQuery);
                        preparedStatement.setString(1, txtEnrollmentID.getText());

                        preparedStatement.executeUpdate();
                        if (showTime) {
                           show_allocation();
                            showTime = false;
                        } else {
                            model.setRowCount(0);
                            show_allocation();
                        }
                        JOptionPane.showMessageDialog(null, "Remove completed!");
                        connection.close();
                    } else {
                        txtEnrollmentID.selectAll();
                        txtEnrollmentID.replaceSelection("");
                    }
                } catch (SQLException e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_btnRunActionPerformed

