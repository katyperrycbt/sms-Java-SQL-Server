    public ArrayList<Allocation> allocationArrayList() {
        ArrayList<Allocation> allocationArrayList = new ArrayList<>();
        try {
            Connection connection = ConnectionClass.connection();
            String sql = "SELECT * FROM Class_allocation";
            Statement preparedStatement = connection.createStatement();
            ResultSet rs = preparedStatement.executeQuery(sql);
            Allocation allocation;
            while (rs.next()) {
                allocation = new Allocation(
                        rs.getInt("enrollment_ID"),
                        rs.getString("subject_ID"),
                        rs.getInt("staff_ID"),
                        rs.getInt("student_ID"),
                        rs.getString("class_ID"),
                        rs.getString("room"));
                allocationArrayList.add(allocation);
            }
            connection.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return allocationArrayList;
    }

    public void show_allocation() {
        ArrayList<Allocation> list = allocationArrayList();
        model = (DefaultTableModel) tableShowAllocation.getModel();
        Object[] row = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getEnrollment_ID();
            row[1] = list.get(i).getSubject_ID();
            row[2] = list.get(i).getStaff_ID();
            row[3] = list.get(i).getStudent_ID();
            row[4] = list.get(i).getClass_ID();
            row[5] = list.get(i).getRoom();
            model.addRow(row);
        }
    }

